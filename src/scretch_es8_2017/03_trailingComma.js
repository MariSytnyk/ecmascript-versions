// ARRAYS
// One trailing comma
const arr1 = [
  1,
  2,
  3,
];

console.log(arr1) // [1, 2, 3]
console.log(arr1.length) // 3

// Several trailing commas
const arr2 = [1, 2, 3, , ,];
console.log(arr2.length) // 5

